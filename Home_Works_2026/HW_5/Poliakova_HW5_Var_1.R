# ЗАДАНИЕ: Представьте себе, что вы идете по лесам Средиземья с отрядом эльфов. Все участники похода были независимо и случайно отобраны из числа этого народа. И вдруг, вдалеке, вы заметили отряд неких существ. Вам необходимо принять решение о том, свои это или чужие. Вы (с помощью магии, наверное) смогли как-то измерить рост фигур из этого отряда. Сделайте обоснованный вывод на основе данных, которые у вас имеются. Результаты необходимо описать в виде текста (комментарии в скрипте с решением, в котором надо сформулировать нулевую и альтернативную гипотезы и сделать выбор между ними) и визуализировать в виде столбчатой диаграммы (необходимо написать код, который ее построит).

# Рост эльфов из вашего отряда (см): 183, 174, 180, 177, 183, 183, 177, 173, 176, 185, 180, 176, 170, 176, 184
# Рост фигур (см): 175, 172, 166, 161, 170, 164, 169, 164, 165, 174, 168, 164, 161, 166, 157



# Нулевая гипотеза: группы одинаковы, разница в росте небольшая, перед нами тоже эльфы.
# Альтернативная гипотеза: группы значимо различаются, перед нами чужаки.
# Инструмент: критерий Стьюдента (t-тест).

library(ggplot2)
our_elves <- c(183, 174, 180, 177, 183, 183, 177, 173, 176, 185, 180, 176, 170, 176, 184)
strangers <- c(175, 172, 166, 161, 170, 164, 169, 164, 165, 174, 168, 164, 161, 166, 157)
test_result <- t.test(our_elves, strangers)
print(test_result)
# Вывод: Разница в росте статистически значима. Встреченный отряд — чужаки (они существенно ниже наших эльфов).

df_plot <- data.frame(
  Group = c("Наш отряд", "Чужаки"),
  MeanHeight = c(mean(our_elves), mean(strangers)),
  SD = c(sd(our_elves), sd(strangers))
)
ggplot(df_plot, aes(x = Group, y = MeanHeight, fill = Group)) +
  geom_col(color = "black", width = 0.6) +
  geom_errorbar(aes(ymin = MeanHeight - SD, ymax = MeanHeight + SD), width = 0.1) +
  scale_fill_manual(values = c("Наш отряд" = "#228B22", "Чужаки" = "#8B4513")) +
  labs(
    title = "Сравнение среднего роста групп в лесах Средиземья",
    subtitle = paste("p-value =", format.pval(test_result$p.value)),
    x = "Отряд",
    y = "Средний рост (см)"
  ) +
  theme_minimal() +
  coord_cartesian(ylim = c(150, 190))
