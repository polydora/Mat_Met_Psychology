# Комментарий: Формулировка гипотез
# Нулевая гипотеза H0: Средний рост эльфов равен среднему росту фигур.
# Альтернативная гипотеза Ha: Средний рост эльфов отличается от среднего роста фигур.

# Загружаем необходимые библиотеки
library(tidyverse) # Для обработки данных и построения графиков
library(gplots)    # Для построения boxplot'ов

# Рост эльфов из вашего отряда
elfs_height <- c(178, 180, 181, 183, 181, 183, 178, 180, 174, 183, 180, 189, 178, 179, 177)

# Рост фигур
figures_height <- c(168, 162, 163, 171, 171, 163, 167, 160, 159, 174, 163, 169)

# Создаем dataframe для удобства работы
df <- data.frame(
  group = c(rep("эльфы", length(elfs_height)), rep("фигуры", length(figures_height))),
  height = c(elfs_height, figures_height)
)

# Построим столбчатую диаграмму
ggplot(df, aes(x=group, y=height, fill=group)) +
  geom_boxplot() +
  ggtitle("Сравнение роста эльфов и фигур") +
  xlab("Группа") +
  ylab("Рост (см)") +
  theme_bw()

# Проверяем гипотезы с помощью t-теста
t.test(height ~ group, data = df, alternative = "two.sided")

# Выводы
# Если p-value < 0.05, отвергаем нулевую гипотезу и принимаем альтернативную,
# что означает, что средние значения роста эльфов и фигур различаются.
